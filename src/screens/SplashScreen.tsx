export function SplashScreen({ navigation, route }: Props) {
  const isFirstTime = route.params?.isFirstTime ?? true;
  // Returning users: initialise to last phrase so the correct text is on screen from frame one.
  // First-time users: 0 is correct — runPhrase() sets it before animating.
  const [phraseIndex, setPhraseIndex] = useState(isFirstTime ? 0 : PHRASES.length - 1);
  const [showButton, setShowButton] = useState(false);
  const activeRef = useRef(true);
  const doneRef = useRef(false);

  const headerOpacity = useRef(new Animated.Value(0)).current;
  const headerY = useRef(new Animated.Value(24)).current;
  // Current phrase anims
  const phraseX = useRef(new Animated.Value(width * 0.45)).current;
  const phraseOpacity = useRef(new Animated.Value(0)).current;
  const buttonOpacity = useRef(new Animated.Value(0)).current;
  const buttonY = useRef(new Animated.Value(16)).current;
  const buttonScale = useRef(new Animated.Value(1)).current;
  const glowOpacity = useRef(new Animated.Value(0)).current;
  const pulseRef = useRef<Animated.CompositeAnimation | null>(null);

  useEffect(() => {
    activeRef.current = true;
    if (!isFirstTime) {
      // Show header + last phrase immediately (no slide-in animation for phrase)
      phraseOpacity.setValue(1);
      phraseX.setValue(0);
      Animated.sequence([
        Animated.parallel([
          Animated.timing(headerOpacity, { toValue: 1, duration: 500, useNativeDriver: true }),
          Animated.timing(headerY,      { toValue: 0, duration: 500, useNativeDriver: true }),
        ]),
        Animated.delay(1200),
      ]).start(() => navigation.replace('Home'));
      return;
    }
    Animated.sequence([
      Animated.delay(300),
      Animated.parallel([
        Animated.timing(headerOpacity, { toValue: 1, duration: 700, useNativeDriver: true }),
        Animated.timing(headerY, { toValue: 0, duration: 700, useNativeDriver: true }),
      ]),
      Animated.delay(400),
    ]).start(() => { if (activeRef.current) runPhrase(0); });
    return () => { activeRef.current = false; pulseRef.current?.stop(); };
  }, []);

  const runPhrase = (index: number) => {
    if (!activeRef.current) return;
    const isLast = index === PHRASES.length - 1;

    // Set text, reset position to right
    setPhraseIndex(index);
    phraseX.setValue(width * 0.45);
    phraseOpacity.setValue(0);

    // Slide in + fade in
    Animated.parallel([
      Animated.timing(phraseX, { toValue: 0, duration: SLIDE_MS, useNativeDriver: true }),
      Animated.timing(phraseOpacity, { toValue: 1, duration: SLIDE_MS, useNativeDriver: true }),
    ]).start(() => {
      if (!activeRef.current) return;
      // Hold fully visible
      setTimeout(() => {
        if (!activeRef.current) return;
        if (isLast && !doneRef.current) {
          // Last phrase stays — show button
          doneRef.current = true;
          showCTA();
        } else if (!isLast) {
          // Start fading out, and simultaneously start next phrase sliding in
          Animated.timing(phraseOpacity, { toValue: 0, duration: FADE_MS, useNativeDriver: true }).start();
          // Next phrase starts immediately as this one begins fading
          setTimeout(() => runPhrase(index + 1), 0);
        }
      }, HOLD_MS);
    });
  };

  const showCTA = () => {
    setShowButton(true);
    Animated.parallel([
      Animated.timing(buttonOpacity, { toValue: 1, duration: 500, useNativeDriver: true }),
      Animated.timing(buttonY, { toValue: 0, duration: 500, useNativeDriver: true }),
    ]).start(() => startGlow());
  };

  const startGlow = () => {
    pulseRef.current = Animated.loop(
      Animated.sequence([
        Animated.parallel([
          Animated.timing(buttonScale, { toValue: 1.06, duration: 900, useNativeDriver: true }),
          Animated.timing(glowOpacity, { toValue: 1, duration: 900, useNativeDriver: true }),
        ]),
        Animated.parallel([
          Animated.timing(buttonScale, { toValue: 1, duration: 900, useNativeDriver: true }),
          Animated.timing(glowOpacity, { toValue: 0.2, duration: 900, useNativeDriver: true }),
        ]),
      ])
    );
    pulseRef.current.start();
  };

  return (
    <LinearGradient colors={['#fdf4f5', '#f8e8ed', '#e8c0cd']} locations={[0, 0.55, 1]} style={styles.container}>
      <StatusBar barStyle="dark-content" />
      <View style={StyleSheet.absoluteFill} pointerEvents="none">
        {SPARKLES.map((s, i) => <Sparkle key={i} {...s} />)}
      </View>

      <Animated.View style={[styles.textArea, { opacity: headerOpacity, transform: [{ translateY: headerY }] }]}>
        <Text style={styles.line1}>What if your</Text>
        <Text style={styles.brandName}>Past.Self.</Text>
        <Text style={styles.couldText}>could...</Text>
      </Animated.View>

      <Animated.View style={[styles.phraseArea, { opacity: phraseOpacity, transform: [{ translateX: phraseX }] }]}>
        <Text style={styles.changingPhrase} numberOfLines={2}>{PHRASES[phraseIndex]}</Text>
      </Animated.View>

      {showButton && (
        <Animated.View style={[styles.buttonArea, { opacity: buttonOpacity, transform: [{ translateY: buttonY }, { scale: buttonScale }] }]}>
          {/* Glow layers — stacked for intensity */}
          <Animated.View style={[styles.glowOuter, { opacity: glowOpacity }]} />
          <Animated.View style={[styles.glowInner, { opacity: glowOpacity }]} />
          <TouchableOpacity
            style={styles.tryBtn}
            onPress={() => { activeRef.current = false; navigation.replace('OnboardingCamera'); }}
            activeOpacity={0.85}
          >
            <Text style={styles.tryBtnText}>Try It Now!</Text>
          </TouchableOpacity>
        </Animated.View>
      )}
    </LinearGradient>
  );
}

