const Sparkle = ({ x, y, delay, size }: { x: number; y: number; delay: number; size: number }) => {
  const opacity = useRef(new Animated.Value(0)).current;
  const scale = useRef(new Animated.Value(0.4)).current;
  const translateY = useRef(new Animated.Value(0)).current;

  useEffect(() => {
    const dur = 2500 + Math.random() * 3000;
    const loop = () => {
      opacity.setValue(0); scale.setValue(0.4); translateY.setValue(0);
      Animated.sequence([
        Animated.delay(delay + Math.random() * 500),
        Animated.parallel([
          Animated.timing(opacity, { toValue: 0.75, duration: dur * 0.35, useNativeDriver: true }),
          Animated.timing(scale, { toValue: 1, duration: dur * 0.35, useNativeDriver: true }),
        ]),
        Animated.parallel([
          Animated.timing(opacity, { toValue: 0, duration: dur * 0.65, useNativeDriver: true }),
          Animated.timing(translateY, { toValue: -10, duration: dur * 0.65, useNativeDriver: true }),
        ]),
      ]).start(() => setTimeout(loop, 1000 + Math.random() * 4000));
    };
    loop();
  }, []);

  return (
    <Animated.View style={{ position: 'absolute', left: x, top: y, opacity, transform: [{ scale }, { translateY }] }}>
      <View style={{ width: size, height: size, borderRadius: size / 2, backgroundColor: '#fff' }} />
    </Animated.View>
  );
};

